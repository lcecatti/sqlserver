CREATE TABLE CLIENTE_TB(
	ID INTEGER IDENTITY(1,1),
	CODIGOCLIENTE NVARCHAR(15),
	NOME VARCHAR(100),
	SOBRENOME VARCHAR(50),
	SEXO CHAR(1),
	DATANASC DATE,
	CPF VARCHAR(11),
	EMAIL VARCHAR(80),
	TELEFONE VARCHAR(11),
	CONSTRAINT PK_CLIENTE PRIMARY KEY(ID)
)
GO
--CODIGO DO CLIENTE É A REFERENCIA DAS 4 PRIMEIRAS LETRAS DO NOME MAIS O CPF

CREATE TABLE CLIENTEACESSO_TB(
	CODIGOCLIENTE NVARCHAR(15),
	CPF VARCHAR(11),
	EMAIL VARCHAR(80),
	SENHA VARCHAR(6)
)
GO
