CREATE TABLE MARCA(
	ID INTEGER IDENTITY,
	NOMEMARCA VARCHAR(100)
	CONSTRAINT PK_MARCA PRIMARY KEY(ID)
)
GO

CREATE TABLE TIPOPRODUTO(
	ID INTEGER IDENTITY,
	NOMETIPOPRODUTO VARCHAR(100),
	CONSTRAINT PK_TIPOPRODUTO PRIMARY KEY(ID)
)
GO

CREATE TABLE PRODUTO
(
	ID INTEGER IDENTITY(1,1),
	CODIGOPRODUTO INTEGER,
	NOMEPRODUTO VARCHAR(100),
	VALORUNITARIO DECIMAL(10,2) NULL,
	IDMARCA INTEGER,
	IDTIPOPRODUTO INTEGER
	CONSTRAINT PK_PRODUTO PRIMARY KEY(ID),
	CONSTRAINT FK_PRODUTOMARCA FOREIGN KEY(IDMARCA) REFERENCES MARCA(ID),
	CONSTRAINT FK_PRODUTOTIPO FOREIGN KEY(IDTIPOPRODUTO) REFERENCES TIPOPRODUTO(ID)
)
GO
--CODIGO DO PRODUTO É A REFERENCIA DAS DO IDMARCA MAIS O IDTIPOPRODUTO

